const mongoose = require("mongoose");

const movieAdminSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  mobile: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
});

const movieAdmin = mongoose.model("Movie_Admin", movieAdminSchema);
module.exports = movieAdmin;
